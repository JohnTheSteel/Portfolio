# Use of index method
print("HELLO".index("L"))
# zwraca INDEKS PIERWSZEGO WYSTAPIENIA
# okreslonej wartosci w (liscie,stringuetc.), gdzie
#  H - 0      pierwsze "L" ma indeks 2, wiec ta 
#  E - 1     liczba powinna byc zwrocona jako wynik
#  L - 2
#  L - 3
#  O - 4

# Slice of index [1:4]
print("HELLO"[1:4])
# zwraca wartosci o okreslonym przez nas
# indeksie, w tym przypadku przedział 1 do 4,
# ale z wyłączeniem 4, gdzie
# indeks zaczyna się od liczby "0", więc
#  H - 0
#  E - 1
#  L - 2
#  L - 3
#  O - 4